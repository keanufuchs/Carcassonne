import * as THREE from 'three';
import type { TilePrototype } from '../core/types/tile';
import type { TileRegions } from './svgRegions';
import { BASE_COLOR, TILE_SIZE, DETAIL } from './palette';
import { makeRng, svgToWorld, standard, centroid, type World2 } from './generators/util';
import { generateCity, cityBanner } from './generators/city';
import { generateField } from './generators/field';
import { generateRoad } from './generators/road';
import { generateMonastery } from './generators/monastery';

/**
 * Iteration 3/4 — procedural content per segment kind. The TS topology (`proto`)
 * provides semantics and a deterministic seed; the SVG-derived `regions`
 * provide boundaries. Each kind is generated by its own module under
 * `generators/`. City walls follow only the true outer boundary (tile-edge
 * connections and internal seams are excluded), and fields don't scatter
 * greenery underneath cities.
 */

/** The ground plate the tile sits on (slightly inset below y=0). */
function basePlate(): THREE.Mesh {
  const geometry = new THREE.BoxGeometry(TILE_SIZE, 0.04, TILE_SIZE);
  geometry.translate(0, -0.02, 0);
  const mesh = new THREE.Mesh(geometry, standard(BASE_COLOR));
  mesh.receiveShadow = true;
  return mesh;
}

/** Adds one shield banner per shielded city (parts of a city share a localId). */
function addShieldBanners(group: THREE.Group, proto: TilePrototype, regions: TileRegions): void {
  const done = new Set<number>();
  for (const region of regions.polygons) {
    if (region.kind !== 'CITY') continue;
    const shielded = proto.segments.find((s) => s.localId === region.localId)?.isShielded === true;
    if (!shielded || done.has(region.localId)) continue;
    done.add(region.localId);
    const parts = regions.polygons
      .filter((r) => r.kind === 'CITY' && r.localId === region.localId)
      .flatMap((r) => r.points.map(svgToWorld));
    group.add(...cityBanner(centroid(parts), DETAIL.cityBaseHeight));
  }
}

export function generateTile(proto: TilePrototype, regions: TileRegions): THREE.Group {
  const group = new THREE.Group();
  group.name = `tile-${proto.id}`;
  group.add(basePlate());

  const cityPolys: World2[][] = regions.polygons
    .filter((r) => r.kind === 'CITY')
    .map((r) => r.points.map(svgToWorld));

  // CITY / FIELD areas → procedural settlements / countryside.
  for (const region of regions.polygons) {
    const rng = makeRng(`${proto.id}:${region.kind}:${region.localId}`);
    const poly = region.points.map(svgToWorld);
    const objects = region.kind === 'CITY'
      ? generateCity(poly, rng, cityPolys)
      : generateField(poly, rng, cityPolys);
    group.add(...objects);
  }

  addShieldBanners(group, proto, regions);

  // ROAD → ribbon + curb along the centreline.
  for (const road of regions.roads) {
    group.add(...generateRoad(road.centerline.map(svgToWorld), road.width / 100));
  }

  // MONASTERY → cloister building at the marker point.
  for (const marker of regions.markers) {
    group.add(...generateMonastery(svgToWorld(marker.pos)));
  }

  return group;
}
